    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        String course = (String)Course.getSelectedItem();
        String branchName=(String)Branch.getSelectedItem();
        String roll = Rollno.getText();
        String name = Name.getText();
        String fatherName = Fathername.getText();
        String gender = (String)Gender.getSelectedItem();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","12345");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,fathername,gender) values('"+roll+"' , '"+course+"' , '"+branchName+"' , '"+name+"' , '"+fatherName+"' , '"+gender+"')");
            JOptionPane.showMessageDialog(null , "Data saved successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null , e.toString());
        }
    }//GEN-LAST:event_SaveActionPerformed

